public class Objective3Lab2 {
	public static void main (String[] args) {
		double side1, side2, hypotenuse;  // declare variables side1, side2, hypotenuse as double
		side1 = 8;			// initialize side1 variable			
		side2 = 19;			// initialize side2 variable
		hypotenuse = Math.sqrt((side1 * side1) + (side2 * side2));  // initialize hypotenuse variable as equation of right triangle a^2+b^2=c^2
		System.out.println("The hypotenuse of a trinagle with sides " + side1 + " and " + side2 + " is " + hypotenuse);		// print out answer





	}
}