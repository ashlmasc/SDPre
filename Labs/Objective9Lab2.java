public class Objective9Lab2 {
  public static void main(String[] args) { // Main method
    System.out.println("In main");    // Prints out message In main
    printMessage();   // Calls printMessage method
    System.out.println("In main");    // Prints out message In main
  }

  public static void printMessage() {   // Method printMessage that prints out message In method
    System.out.println("In method");
  }
}