public class Objective8Lab1 {
  public static void main(String[] args) {
    
    for (int i=1; i<=10; i+=1){   // for loop that declares and initializes the integer i to 1, excutes the loop while i is less than or equal to 10, and increases i by 1 increment each time the loop executes
      System.out.println(i);      // Prints out i
    }

    for (int i=10; i>=1; i-=1){   // for loop that declares and initializes the integer i to 10, executes the loop while i is greater than or equal to 1, and decreases i by 1 increment each time the loop executes
      System.out.println(i);      // Prints out i
    }
  }
}